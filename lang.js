let currentLang = localStorage.getItem('usp-lang') || 'en';

function applyLang(lang) {
  const d = LANG[lang];
  currentLang = lang;
  localStorage.setItem('usp-lang', lang);

  // Nav
  document.querySelectorAll('[data-t]').forEach(el => {
    const key = el.getAttribute('data-t');
    if (!d[key]) return;
    if (el.tagName === 'INPUT') el.placeholder = d[key];
    else if (el.tagName === 'BUTTON' && key === 'form.btn') el.textContent = d[key];
    else el.innerHTML = d[key];
  });

  // Toggle button text
  const btn = document.getElementById('langBtn');
  if (btn) btn.textContent = lang === 'en' ? 'RU' : 'EN';
}

window.toggleLang = function() {
  applyLang(currentLang === 'en' ? 'ru' : 'en');
};

document.addEventListener('DOMContentLoaded', () => applyLang(currentLang));
